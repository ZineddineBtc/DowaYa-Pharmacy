package com.example.dowaya_pharmacy.models;

public class User {

    private String id, username, email, phone, address, city,
            searchHistoryTime;

    public User(){}

    public String getSearchHistoryTime() {
        return searchHistoryTime;
    }

    public void setSearchHistoryTime(String searchHistoryTime) {
        this.searchHistoryTime = searchHistoryTime;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
